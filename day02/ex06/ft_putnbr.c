void	ft_putnbr(int nb)
{
	int i;
	int temp;

	i = 1000000000;
	temp = nb;
	if (nb < 0)
	{
		ft_putchar('-');
		temp = temp * (-1);
	}
	while (i >= 1)
	{
		if ((temp / i) != 0)
		{
			ft_putchar((char)((temp / i) + '0'));
			temp = temp % i;
		}
		i = i / 10;
	}
}
