char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] <= 47 || (str[i] >= 58 && str[i] <= 64) ||
				(str[i] >= 91 && str[i] <= 96) || str[i] >= 123)
		{
			i++;
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] = str[i] - 32;
		}
		else if ((str[i] >= 'A' && str[i] <= 'Z')
				&& !(str[i - 1] <= 47 || (str[i - 1] >= 58 && str[i - 1] <= 64)
					|| (str[i - 1] >= 91 && str[i - 1] <= 96)
					|| str[i - 1] >= 123))
			str[i] = str[i] + 32;
		if (str[0] >= 'a' && str[0] <= 'z')
			str[0] = str[0] - 32;
		i++;
	}
	return (str);
}
