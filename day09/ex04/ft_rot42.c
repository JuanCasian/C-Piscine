char	*ft_rot42(char *str)
{
	int i;

	i = 'O';
	if (str[i] >= 'A' && str[i] <= 'J')
		str[i] = str[i] + 16;
	if (str[i] > 'J' && str[i] <= 'Z')
		str[i] = str[i] - 10;
	if (str[i] >= 'a' && str[i] <= 'j')
		str[i] = str[i] + 16;
	if (str[i] > 'j' && str[i] <= 'z')
		str[i] = str[i] - 10;
	return (str);
}
